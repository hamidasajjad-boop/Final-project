public record elseif() {
    
public static void main(String[] args)
{

int grade= 11;

if(grade==10)
{
    System.out.print("You have 2 electives and your grade is "+grade);
}

else if(grade==11)
{
    System.out.print("You have 3 electives and your grade is "+grade);
}

else if(grade==12)
{
    System.out.print("You have 5 electives and your grade is "+grade);
}
else
{
    System.out.print("You are not eligible for electives as your grade is "+grade);
}








}

}
 